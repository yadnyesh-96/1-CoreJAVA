// Q54. Write a java program to display following series :
//           	1  2  4  7  11  16  22  29  37  46.


public class QuestionFiftyFour{
	
		public static void main(String x[]){
			int i = 0;
			int sum =1;
			
			while(i<=9){
				
				sum=sum+i;
				System.out.println(sum);
				i++;
				
			}
			
		}


}